export function slugify(text) {
  if (typeof text !== 'string') {
    return '';
  }

  return text
    .toString() // Ensure input is a string
    .toLowerCase() // 1. 모든 문자를 소문자로 변환
    .replace(/\s+/g, '-') // 2. 하나 이상의 공백을 대시(-)로 치환
    .replace(/[^\w\\-]+/g, '') // 3. 단어 문자(a-z, 0-9, _)와 대시(-)가 아닌 문자는 모두 제거
    .replace(/\\-\\-+/g, '-') // 4. 두 개 이상 연속된 대시를 하나로 치환
    .replace(/^-+/, '') // 5. 문자열 시작에 있는 대시를 제거
    .replace(/-+$/, ''); // 6. 문자열 끝에 있는 대시를 제거
}

/*
    - \s: '공백(space)'을 의미하는 특수 문자입니다. 여기에는 스페이스바, 탭(\t), 줄바꿈(\n) 등이 모두 포함됩니다.
    - +: '하나 이상 연속'을 의미하는 수량자
         \s+는 공백이 1개, 2개, 100개든 상관없이 연속된 공백 덩어리를 하나로 인식합니다.
    - /.../g: 정규식 패턴의 양 끝을 감싸는 /와, 패턴 전체에 대한 '전역(Global)' 검색을 의미하는 g 플래그
              g가 없으면 문자열에서 첫 번째로 일치하는 부분만 바꾸고 끝나지만, g가 있으면 일치하는 모든 부분을 찾아서 바꿉니다.
              예시) " Bye " → replace → "-Bye-" (앞뒤 공백도 대시로)
    - [...] : 문자집합
    - [^...] : 문자 집합 안에서 맨 앞에 쓰일 경우, 부정을 의미 
    - \w : 단어문자. [a-zA-Z0-9]와 같은 의미 
    - \- : 문자집합내에서 -는 연속되는 의미기에, 대시 표현하려면 \ 붙여야함 
    - /[^\w\-]+/g: 연속된 특수문자를 전역 검색. 
    /\-\-+/g : 두개 이상의 연속된 대시를 찾음
    - ^ : 문자집합 내에서 사용하면 부정, BUT 밖에서 사용하면 문자열의 첫 시작을 뜻함 
    - /^-+/ : 문자열의 시작에 연속된 리터럴 대시 찾음
    - $: 문자열의 끝 
    - /-+$/ 끝부분 대시 제거 
*/