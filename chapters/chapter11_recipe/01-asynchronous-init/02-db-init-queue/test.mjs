// 1. 스크립트가 처음 실행되면, 이 코드는 콜 스택에 바로 올라가 실행됩니다.
console.log('1️⃣ [콜 스택] 동기 코드: 스크립트 시작');

// 2. setTimeout 함수가 호출됩니다.
// - 브라우저/Node.js의 Web API(백그라운드)에 "0초 뒤에 이 콜백을 실행해줘"라고 요청합니다.
// - Web API는 타이머가 완료되면 콜백 함수를 [매크로태스크 큐]로 보냅니다.
// - 중요: 0초라도, 콜백은 즉시 실행되지 않고 항상 큐를 거칩니다.
setTimeout(() => {
  console.log('5️⃣ [매크로태스크] setTimeout 콜백 실행');
}, 0);

// 3. Promise가 생성되고 즉시 'fulfilled' 상태가 됩니다.
// - .then() 안의 콜백 함수는 즉시 실행되지 않고, [마이크로태스크 큐]로 보내집니다.
Promise.resolve().then(() => {
  console.log('4️⃣ [마이크로태스크] Promise.then 콜백 실행');
});

// 4. async 함수를 호출합니다.
// - 함수 자체는 콜 스택에 바로 올라가 동기적으로 실행됩니다.
async function runAsync() {
  console.log('2️⃣ [콜 스택] 동기 코드: runAsync 함수 내부');
  
  // await는 Promise.resolve()가 완료되기를 기다립니다. (즉시 완료됨)
  // 'await' 키워드는 이 줄 뒷부분의 코드를 [마이크로태스크 큐]로 보내는 역할을 합니다.
  await Promise.resolve();

  // 이 코드는 마이크로태스크 큐를 통해 실행됩니다.
  console.log('4️⃣-2 [마이크로태스크] await 이후 코드 실행');
}

runAsync();

// 5. 스크립트의 모든 동기적 코드가 여기까지 실행되었습니다.
// - 이제 자바스크립트 엔진은 콜 스택이 비었으므로, 마이크로태스크 큐를 확인하러 갑니다.
console.log('3️⃣ [콜 스택] 동기 코드: 스크립트 종료');


